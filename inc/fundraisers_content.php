    <p class="info">
      At Rotary, we have started our annual potato fundraiser. This is a key fundraiser for our club as it bings in
      <b>$70,000</b> for us to put towards community projects that can benefiet schools, charities and communities.
      <br>
      <p>
      <b>History:</b> We have been growing, harvesting and selling delicious new Cliff Kidney potatoes at Christmas since 2012.
      We have club members who have the expertise and machinery to enable this project to be the success it has been.
      As well, all members can contribute in some way, whether it is in the growing, packing or selling.
      The potatoes are now becoming very well known around town and we have many queries each year as to when will they be available.
      We sell several thousand of the 2kg boxes each year and all proceeds go to charity.
